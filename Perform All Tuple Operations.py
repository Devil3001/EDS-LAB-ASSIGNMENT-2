# Creating tuple
t = (10, 20, 30, 40, 50)

# Access
print("First Element:", t[0])

# Length
print("Length:", len(t))

# Count
print("Count of 20:", t.count(20))

# Index
print("Index of 30:", t.index(30))

# Concatenation
t2 = t + (60, 70)
print("After Concatenation:", t2)

# Repetition
print("Repetition:", t * 2)

# Slicing
print("Slicing:", t[1:4])
